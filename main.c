void bodyOrientation(float degp,float degr)
{

    float l = 0.13505;
    float b = 0.119615;
    float FL[]={l,0,b};
    float BL[]={-l,0,b};
    float FR[]={l,0,-b};
    float BR[]={-l,0,-b};
    float T[3][3];
    float FT0[]={0,0,0};
    float FT1[]={0,0,0};
    float FT2[]={0,0,0};
    float FT3[]={0,0,0};
    int i,j,k;
    float Tr[3][3]={
        {1,0,0},
        {0,cosf(degr),sinf(degr)},
        {0,-sinf(degr),cosf(degr)}
    };
    float Tp[3][3]={
        {cosf(degp),sinf(degp),0},
        {-sinf(degp),cosf(degp),0},
        {0,0,1}
    };
    for(i=0; i<3; ++i)
        for(j=0; j<3; ++j)
        {
            T[i][j] = 0;
        }
    for(i=0; i<3; ++i)
        for(j=0; j<3; ++j)
            for(k=0; k<3; ++k)
            {
                T[i][j]+=Tr[i][k]*Tp[k][j];
            }

    for(i=0; i<3; ++i)
        for(k=0; k<3; ++k)
        {
            FT0[i]+=T[i][k]*FL[k];
        }

    for(i=0; i<3; ++i)
        for(k=0; k<3; ++k)
        {
            FT1[i]+=T[i][k]*BL[k];
        }

    for(i=0; i<3; ++i)
        for(k=0; k<3; ++k)
        {
            FT2[i]+=T[i][k]*FR[k];
        }

    for(i=0; i<3; ++i)
        for(k=0; k<3; ++k)
        {
            FT3[i]+=T[i][k]*BR[k];
        }

    flyoffset=FT0[1];
    blyoffset=FT1[1];
    fryoffset=FT2[1];
    bryoffset=FT3[1];

}


