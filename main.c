float limiter(float X)
{
    //function returns -1 if x < 0, 0 if x==0, 1 if x > 0.
    float temp=X;
    if(temp<0)
        temp=temp*-1;
    if(temp>1){
        if(X<0)
            return -1;
        else if(X==0)
            return 0;
        else if(X==0)
            return 1;
    }
    else
        return X;
}

